# 2.2.1变量命名

## 变量名只能包含数字，字母，下划线；且不能以数字开头；
## 变量名不能包含空格；
## 变量名不能以关键字和函数名来命名；
## 变量名要有语义性；
## 变量名慎用 小写字母 l 和字母 o ，易被认为 1 和 0；
## 变量名尽量以小写字母命名，下划线分割单词；



