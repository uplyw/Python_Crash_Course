#数字
#数字包括整数和浮点数
# 数字的运算包括加、减、乘、除、乘方

#整数
print(3+2)				# 5
print(3-2)				# 1
print(3*2)				# 6
print(3/2)				# 1.5
print(3**2)				# 9
print((3+2)*2)			# 10

#浮点数
print(0.1+0.1)			# 0.2
print(0.2+0.1)			# 0.30000000000000004

